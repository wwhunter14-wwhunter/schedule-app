function toInt(date: Date): number {
  return date.getFullYear() * 10000 + (date.getMonth() + 1) * 100 + date.getDate()
}

export function getLunarDateStr(date: Date): string {
  const target = toInt(date)

  // 이진 탐색으로 해당 날짜가 속한 음력월 찾기
  let lo = 0
  let hi = LUNAR_MONTH_STARTS.length - 1

  while (lo < hi) {
    const mid = Math.floor((lo + hi + 1) / 2)
    if (LUNAR_MONTH_STARTS[mid][0] <= target) {
      lo = mid
    } else {
      hi = mid - 1
    }
  }

  const [startSolar, lunarYear, lunarMonth, isLeap] = LUNAR_MONTH_STARTS[lo]

  // 음력 일 계산 (양력 날짜 - 음력월 시작일 + 1)
  const startDate = new Date(
    Math.floor(startSolar / 10000),
    Math.floor((startSolar % 10000) / 100) - 1,
    startSolar % 100
  )
  const diffMs = date.getTime() - startDate.getTime()
  const lunarDay = Math.floor(diffMs / 86400000) + 1

  const leapStr = isLeap ? '윤' : ''
  return `음력 ${leapStr}${lunarMonth}월 ${lunarDay}일`
}
